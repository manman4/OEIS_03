# nまでの分割数
def a000041(n)
  ary = [1]
  (1..n).each{|i|
    s = 0
    (1..i).each{|j|
      k = j*(3*j-1)/2
      break if k > i
      s += (-1)**(j-1)*ary[i-k]
      k = j*(3*j+1)/2
      break if k > i
      s += (-1)**(j-1)*ary[i-k]
    }
    ary << s
  }
  ary
end

# a(n) = Sum_{k=0..n} A396075(k) * A000041(n-k).
def a(n)
  a396075 = a396075(n)
  a000041 = a000041(n)
  (0..n).map{|i|
    s = 0
    (0..i).each{|j|
      s += a396075[j] * a000041[i-j]
    }
    s
  }
end

